package com.code.ds.linkedlist;

public class SingleNode {

  private int val;
  private SingleNode next;

  public int getVal() {
    return val;
  }

  public void setVal(int val) {
    this.val = val;
  }

  public SingleNode getNext() {
    return next;
  }

  public void setNext(SingleNode next) {
    this.next = next;
  }

}
